    private void loadTable()
    {
        DefaultTableModel dtm = (DefaultTableModel) this.tblMS.getModel();
        for (MauSac ms : this.msRepo.findAll()) {
            Object[] row = {
                ms.getId(),
                ms.getMa(),
                ms.getTen(),
                ms.getTrangThai(),
            };
            
            dtm.addRow(row);
        }
    }

