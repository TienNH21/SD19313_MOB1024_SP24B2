    private void tblSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSVMouseClicked
        int row = this.tblSV.getSelectedRow();
        
        if (row == -1) {
            return ;
        }
        
        String ma = this.tblSV.getValueAt(row, 0).toString();
        String ten = this.tblSV.getValueAt(row, 1).toString();
        String gioiTinh = this.tblSV.getValueAt(row, 2).toString();
        String cNganh = this.tblSV.getValueAt(row, 3).toString();
        
        this.txtMaSV.setText(ma);
        this.txtHoTen.setText(ten);
        this.txtGioiTinh.setText(gioiTinh);
        this.cbbCNganh.setSelectedItem(cNganh);
    }//GEN-LAST:event_tblSVMouseClicked

    private void btnLamMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLamMoiActionPerformed
        this.clearForm();
    }//GEN-LAST:event_btnLamMoiActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = this.tblSV.getSelectedRow();
        
        if (row == -1) {
            return ;
        }
        
        String ma = this.tblSV.getValueAt(row, 0).toString();
        
        for (int i = 0; i < this.listSV.size(); i++) {
            SinhVien sv = this.listSV.get(i);
            if (sv.getMa().equals(ma)) {
                this.listSV.remove(i);
                break;
            }
        }
        
        this.loadTable();
        this.clearForm();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ma = this.txtMaSV.getText();
        String ten = this.txtMaSV.getText();
        String gt = this.txtMaSV.getText();
        String cNganh = this.cbbCNganh.getSelectedItem().toString();
        SinhVien sv = new SinhVien(ma, ten, gt, cNganh);
        this.listSV.add(sv);
        this.loadTable();
        this.clearForm();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String ma = this.txtMaSV.getText();
        String ten = this.txtMaSV.getText();
        String gt = this.txtMaSV.getText();
        String cNganh = this.cbbCNganh.getSelectedItem().toString();
        SinhVien newSV = new SinhVien(ma, ten, gt, cNganh);

        for (int i = 0; i < this.listSV.size(); i++) {
            SinhVien oldSV = this.listSV.get(i);
            if (oldSV.getMa().equals(newSV.getMa())) {
                this.listSV.set(i, newSV);
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void clearForm()
    {
        this.txtMaSV.setText("");
        this.txtHoTen.setText("");
        this.txtGioiTinh.setText("");
        this.cbbCNganh.setSelectedIndex(0);
    }
    
