    private void loadTable()
    {
        DefaultTableModel dtm =
            (DefaultTableModel) this.tblSV.getModel();
        
        dtm.setRowCount(0);
        for (SinhVien sv : listSV) {
            Object[] row = {
                sv.getMa(),
                sv.getTen(),
                sv.getGioiTinh(),
                sv.getChuyenNganh(),
            };
            
            dtm.addRow(row);
        }
    }

